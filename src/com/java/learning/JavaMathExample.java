package com.java.learning;

public class JavaMathExample {
	
	public static void main(String args[]) {
		double x = 3.56;
		double y = 10.25;
		
		
		System.out.println("The largest number from x and y " + Math.max(x, y));
		System.out.println("The smallest number from x and y " + Math.min(x, y));
		System.out.println("The pow value is  " + Math.pow(x, y));
		System.out.println("The square of x and y " + Math.sqrt(y) + " " +  Math.sqrt(x));
		System.out.println(Math.abs(y));
		
	}

}
